public class Motivo {

    int codEmp;
    int codMtv;
    String desMtv;
    int codAmp;
    String desAmp;

    public int getCodEmp() {
        return codEmp;
    }

    public void setCodEmp(int codEmp) {
        this.codEmp = codEmp;
    }

    public int getCodMtv() {
        return codMtv;
    }

    public void setCodMtv(int codMtv) {
        this.codMtv = codMtv;
    }

    public String getDesMtv() {
        return desMtv;
    }

    public void setDesMtv(String desMtv) {
        this.desMtv = desMtv;
    }

    public int getCodAmp() {
        return codAmp;
    }

    public void setCodAmp(int codAmp) {
        this.codAmp = codAmp;
    }

    public String getDesAmp() {
        return desAmp;
    }

    public void setDesAmp(String desAmp) {
        this.desAmp = desAmp;
    }

}
