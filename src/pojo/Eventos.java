package pojo;

/**
 *
 * @author diogo.melo
 */
public class Eventos {

    int codEve;
    String nomeEve;
    String horEve;

    public int getCodEve() {
        return codEve;
    }

    public void setCodEve(int codEve) {
        this.codEve = codEve;
    }

    public String getNomeEve() {
        return nomeEve;
    }

    public void setNomeEve(String nomeEve) {
        this.nomeEve = nomeEve;
    }

    public String getHorEve() {
        return horEve;
    }

    public void setHorEve(String horEve) {
        this.horEve = horEve;
    }

}
